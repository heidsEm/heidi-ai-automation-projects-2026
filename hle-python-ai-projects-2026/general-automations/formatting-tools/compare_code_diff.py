import difflib


def compare_codes(code1: str, code2: str):
    code1_lines = code1.strip().splitlines()
    code2_lines = code2.strip().splitlines()

    print("\n==== Line-level Differences (Unified Diff) ====\n")
    diff = difflib.unified_diff(code1_lines, code2_lines, fromfile='Code1', tofile='Code2', lineterm='')
    for line in diff:
        print(line)

    print("\n==== Inline Differences (Within Lines) ====\n")
    sm = difflib.SequenceMatcher(None, code1, code2)
    for tag, i1, i2, j1, j2 in sm.get_opcodes():
        if tag != 'equal':
            print(f"{tag.upper()}:")
            print(f"Code1[{i1}:{i2}]:", repr(code1[i1:i2]))
            print(f"Code2[{j1}:{j2}]:", repr(code2[j1:j2]))
            print()

